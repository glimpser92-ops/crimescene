// 결정적 단서(5, 19, 20)는 서로 다른 형사에게. 기준 단서 1번은 20번 소지자와
// 다른 형사에게 배정해 반드시 대화로 맞춰 보게 만든다.
function distributeClues(detectiveIds) {
  const D = detectiveIds.length;
  if (D === 0) return {};
  const byId = Object.fromEntries(CASE.clues.map((c) => [c.id, c]));
  const decisive = [5, 19, 20];
  const rest = CASE.clues.map((c) => c.id).filter((id) => !decisive.includes(id));

  const shuffle = (a) => {
    for (let i = a.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [a[i], a[j]] = [a[j], a[i]];
    }
    return a;
  };

  const order = shuffle([...detectiveIds]);
  const assign = Object.fromEntries(detectiveIds.map((id) => [id, []]));

  // 1) 결정적 단서를 앞쪽 형사들에게 분산 (D < 3이면 겹침 허용)
  shuffle(decisive).forEach((clueId, i) => {
    assign[order[i % D]].push(clueId);
  });

  // 2) 나머지를 라운드로빈으로, 항상 단서 수가 가장 적은 형사부터
  shuffle(rest);
  // 단서 1번(기준 파편)은 20번 소지자를 피해서 먼저 배정
  const holder20 = order.find((id) => assign[id].includes(20));
  if (D > 1) {
    const idx1 = rest.indexOf(1);
    rest.splice(idx1, 1);
    const candidates = order.filter((id) => id !== holder20);
    candidates.sort((a, b) => assign[a].length - assign[b].length);
    assign[candidates[0]].push(1);
  }
  for (const clueId of rest) {
    const sorted = [...order].sort((a, b) => assign[a].length - assign[b].length);
    assign[sorted[0]].push(clueId);
  }

  // 단서 번호순 정렬
  for (const id of detectiveIds) assign[id].sort((a, b) => a - b);
  return assign;
}

